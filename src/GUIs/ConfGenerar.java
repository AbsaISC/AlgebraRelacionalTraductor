    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        boolean anflex = true, anCup = true;
        if (this.pathCup == "" & this.pathFlex == "") {
            JOptionPane.showMessageDialog(this, "No ha seleccionado archivos a procesar", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (this.pathCup == "") {
            JOptionPane.showMessageDialog(this, "No se generaran los archivos correspondientes \n al analizador Sintáctico", "Advertencia", JOptionPane.INFORMATION_MESSAGE);
            anCup = false;
        }
        if (this.pathFlex == "") {
            JOptionPane.showMessageDialog(this, "No se generaran los archivos correspondientes \n al analizador Léxico", "Advertencia", JOptionPane.INFORMATION_MESSAGE);
            anflex = false;
        }

        String[] alexico = {this.pathFlex};
        String[] asintactico = {"-parser", "AnalizadorSintactico", this.pathCup};

        try {
            if (anflex) {
                jflex.Main.main(alexico);
            }
            if (anCup) {
                java_cup.Main.main(asintactico);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro: ", JOptionPane.ERROR_MESSAGE);
            return;
        }
        JOptionPane.showMessageDialog(this, "Archivos generados con éxito", "Aviso", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton6ActionPerformed

